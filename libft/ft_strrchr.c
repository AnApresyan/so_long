#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*result;
	int		i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
			break ;
		if (i == 0)
			return (NULL);
		i--;
	}
	result = &((char *)s)[i];
	return (result);
}
